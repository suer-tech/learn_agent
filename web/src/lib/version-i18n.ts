export function localizeMeta<T extends { title: string; subtitle: string; coreAddition: string; keyInsight: string }>(
  meta: T,
  versionId: string,
  _locale?: string
): T {
  const m = RU[versionId];
  if (!m) return meta;
  return { ...meta, ...m };
}

export function getVersionSummary(versionId: string): string | undefined {
  return SUMMARIES[versionId];
}

export function getVersionRuTitle(versionId: string): string | undefined {
  return RU_TITLES[versionId];
}

export function useLocalizedVersion(version: AgentVersion): AgentVersion {
  const locale = useLocale();
  return localizeMeta(version, version.id, locale);
}
